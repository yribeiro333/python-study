'''
Python = Programming Language
Typing type = Dynamic / Strong
str -> string -> text
Strings are texts that are enclosed in quotation marks
'''
print(1234)

# Single quotes
print('Henrique')
print(1, 'Henrique "Ribeiro"') # Best Alternative


# Double quotes
print("Henrique")
print(2, "Henrique 'Ribeiro'") # Best Alternative


# Escape
print('Henrique \"Ribeiro\"')

# r
print(r'Henrique \"Ribeiro\"')
