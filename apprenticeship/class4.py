# Types int and float
# int -> whole number
# The int type represents any number
# Positive or Negative. int no sing is considered positive.

print(10) # int
print(10, 20) # int
print(-10) # int
print(0) # int

# float -> floating point number
# The float type represents any number
# Positive or Negative with floating point.
# float no sing is considered positive.

print(1.1) # float
print(1.1, 2.2) # float
print(-1.1) # float

#the type function shows the type that python inferred to the value.

print(type('Henrique')) # class > str
print(type('17')) # class > str
print(type(10)) # class > int
print(type(-10)) # class > int
print(type(3.4)) # class > float
print(type(-2.5)) # class > float