# bool data type (boolean)
# When questioning something in a program,
# there are oly two possible answers:
# yes (True) or no (False).
# There are several operators to "question"
# Among them ==, it is a logical operator that,
# asks whether one value is equal to another.

print(10 == 10) # Yes -> True
print(10 == 1) # No -> False
print(type(10 == 10))
print(type(10 == 1))
print(type(True))
print(type(False))